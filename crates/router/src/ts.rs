//! TypeScript client generation.
//!
//! Collects every referenced declaration (transitively, deduped, in name order)
//! then emits the typed maps a TS client generic over `Router` infers from:
//! `ActionParameters`, `ActionResults`, `ActionKinds`, `ClientActionParameters`,
//! `ClientActionResults`.

use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::sync::Arc;

use hashbrown::HashMap;

use crate::action::{ErasedAction, ErasedStreamAction};
use crate::client::ClientMeta;

pub(crate) fn generate<G, R>(
    actions: &HashMap<&'static str, Arc<dyn ErasedAction<G, R>>>,
    streams: &HashMap<&'static str, Arc<dyn ErasedStreamAction<G, R>>>,
    client_actions: &[ClientMeta],
) -> String {
    let mut out = String::from("// Generated by stakit-router. Do not edit.\n\n");

    let mut unary: Vec<&'static str> = actions.keys().copied().collect();
    unary.sort_unstable();
    let mut stream: Vec<&'static str> = streams.keys().copied().collect();
    stream.sort_unstable();
    let mut clients: Vec<&ClientMeta> = client_actions.iter().collect();
    clients.sort_unstable_by_key(|c| c.name);

    // 1. every declaration this router pulls in (self + nested), deduped + sorted.
    let mut decls: BTreeMap<String, String> = BTreeMap::new();
    for name in &unary {
        actions[*name].collect_ts(&mut decls);
    }
    for name in &stream {
        streams[*name].collect_ts(&mut decls);
    }
    for c in &clients {
        for (key, value) in &c.decls {
            decls.insert(key.clone(), value.clone());
        }
    }
    for decl in decls.values() {
        out.push_str(decl);
        out.push_str("\n\n");
    }

    // 2. typed maps (each value is a type *reference*).
    out.push_str("export interface ActionParameters {\n");
    for name in &unary {
        let _ = writeln!(out, "  {name}: {};", actions[*name].input_ref());
    }
    for name in &stream {
        let _ = writeln!(out, "  {name}: {};", streams[*name].input_ref());
    }
    out.push_str("}\n\nexport interface ActionResults {\n");
    for name in &unary {
        let _ = writeln!(out, "  {name}: {};", actions[*name].output_ref());
    }
    for name in &stream {
        let _ = writeln!(out, "  {name}: {};", streams[*name].item_ref());
    }
    out.push_str("}\n\nexport interface ActionKinds {\n");
    for name in &unary {
        let _ = writeln!(out, "  {name}: \"unary\";");
    }
    for name in &stream {
        let _ = writeln!(out, "  {name}: \"stream\";");
    }
    out.push_str("}\n\nexport interface ClientActionParameters {\n");
    for c in &clients {
        let _ = writeln!(out, "  {}: {};", c.name, c.params_ref);
    }
    out.push_str("}\n\nexport interface ClientActionResults {\n");
    for c in &clients {
        let _ = writeln!(out, "  {}: {};", c.name, c.return_ref);
    }
    out.push_str("}\n\n");

    // 3. the single Router type a client infers from.
    out.push_str(concat!(
        "export interface Router {\n",
        "  serverActions: {\n",
        "    [K in keyof ActionParameters]: {\n",
        "      params: ActionParameters[K];\n",
        "      result: ActionResults[K];\n",
        "      kind: ActionKinds[K];\n",
        "    };\n",
        "  };\n",
        "  clientActions: {\n",
        "    [K in keyof ClientActionParameters]: {\n",
        "      params: ClientActionParameters[K];\n",
        "      return: ClientActionResults[K];\n",
        "    };\n",
        "  };\n",
        "}\n",
    ));
    out
}
