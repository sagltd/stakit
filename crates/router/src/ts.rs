//! TypeScript client generation.
//!
//! Collects every referenced declaration (transitively, deduped, in name order)
//! then emits the typed maps a TS client generic over `Router` infers from:
//! `ActionParameters`, `ActionResults`, `ActionKinds`, `ClientActionParameters`,
//! `ClientActionResults`.

use std::collections::{BTreeMap, BTreeSet};
use std::fmt::Write as _;
use std::sync::Arc;

use hashbrown::HashMap;

use crate::action::{ErasedAction, ErasedBorrowAction, ErasedStreamAction};
use crate::client::ClientMeta;
use crate::error::BUILTIN_ERROR_CODES;

/// Streaming-action registry, threaded into the section helpers.
type Streams<G, R> = HashMap<&'static str, Arc<dyn ErasedStreamAction<G, R>>>;

/// Resolved TypeScript metadata for one unary action — owned [`Action`] and
/// zero-copy [`BorrowAction`] alike (both are unary on the wire).
struct Unary {
    name: &'static str,
    input: String,
    output: String,
    codes: &'static [&'static str],
}

pub(crate) fn generate<G, R>(
    actions: &HashMap<&'static str, Arc<dyn ErasedAction<G, R>>>,
    borrow_actions: &HashMap<&'static str, Arc<dyn ErasedBorrowAction<G, R>>>,
    streams: &Streams<G, R>,
    client_actions: &[ClientMeta],
) -> String {
    // 1. every declaration this router pulls in (self + nested), deduped + sorted.
    let mut decls: BTreeMap<String, String> = BTreeMap::new();

    // Unify owned + borrow actions into one unary list (collecting decls as we go).
    let mut unary: Vec<Unary> = Vec::with_capacity(actions.len() + borrow_actions.len());
    for (name, action) in actions {
        action.collect_ts(&mut decls);
        unary.push(Unary {
            name,
            input: action.input_ref(),
            output: action.output_ref(),
            codes: action.error_codes(),
        });
    }
    for (name, action) in borrow_actions {
        action.collect_ts(&mut decls);
        unary.push(Unary {
            name,
            input: action.input_ref(),
            output: action.output_ref(),
            codes: action.error_codes(),
        });
    }
    unary.sort_unstable_by_key(|u| u.name);

    let mut stream: Vec<&'static str> = streams.keys().copied().collect();
    stream.sort_unstable();
    for name in &stream {
        streams[*name].collect_ts(&mut decls);
    }

    let mut clients: Vec<&ClientMeta> = client_actions.iter().collect();
    clients.sort_unstable_by_key(|c| c.name);
    for c in &clients {
        for (key, value) in &c.decls {
            decls.insert(key.clone(), value.clone());
        }
    }

    let mut out = String::from("// Generated by stakit-router. Do not edit.\n\n");
    for decl in decls.values() {
        out.push_str(decl);
        out.push_str("\n\n");
    }
    write_error_envelope(&mut out, &unary, &stream, streams);
    write_interfaces(&mut out, &unary, &stream, streams, &clients);
    out
}

/// The error envelope: a string union of every code the router (built-ins) and
/// any action's error type can emit, plus the typed `ResponseError` shape and a
/// guard. Lets clients exhaustively narrow on `error.type`.
fn write_error_envelope<G, R>(
    out: &mut String,
    unary: &[Unary],
    stream: &[&'static str],
    streams: &Streams<G, R>,
) {
    let mut codes: BTreeSet<&'static str> = BUILTIN_ERROR_CODES.iter().copied().collect();
    for u in unary {
        codes.extend(u.codes);
    }
    for name in stream {
        codes.extend(streams[*name].error_codes());
    }
    let union = codes
        .iter()
        .map(|code| format!("\"{code}\""))
        .collect::<Vec<_>>()
        .join(" | ");
    let _ = write!(out, "export type ErrorCode = {union};\n\n");
    out.push_str(concat!(
        "export interface ResponseError {\n",
        "  code: number;\n",
        "  type: ErrorCode;\n",
        "  message: string;\n",
        "  fields?: Record<string, string[]>;\n",
        "}\n\n",
        "export const isValidationError = (error: ResponseError): boolean =>\n",
        "  error.type === \"validation\";\n\n",
    ));
}

/// The typed maps (each value is a type *reference*) and the single `Router`
/// type a TS client infers from.
fn write_interfaces<G, R>(
    out: &mut String,
    unary: &[Unary],
    stream: &[&'static str],
    streams: &Streams<G, R>,
    clients: &[&ClientMeta],
) {
    out.push_str("export interface ActionParameters {\n");
    for u in unary {
        let _ = writeln!(out, "  {}: {};", u.name, u.input);
    }
    for name in stream {
        let _ = writeln!(out, "  {name}: {};", streams[*name].input_ref());
    }
    out.push_str("}\n\nexport interface ActionResults {\n");
    for u in unary {
        let _ = writeln!(out, "  {}: {};", u.name, u.output);
    }
    for name in stream {
        let _ = writeln!(out, "  {name}: {};", streams[*name].item_ref());
    }
    out.push_str("}\n\nexport interface ActionKinds {\n");
    for u in unary {
        let _ = writeln!(out, "  {}: \"unary\";", u.name);
    }
    for name in stream {
        let _ = writeln!(out, "  {name}: \"stream\";");
    }
    out.push_str("}\n\nexport interface ClientActionParameters {\n");
    for c in clients {
        let _ = writeln!(out, "  {}: {};", c.name, c.params_ref);
    }
    out.push_str("}\n\nexport interface ClientActionResults {\n");
    for c in clients {
        let _ = writeln!(out, "  {}: {};", c.name, c.return_ref);
    }
    out.push_str("}\n\n");
    out.push_str(concat!(
        "export interface Router {\n",
        "  serverActions: {\n",
        "    [K in keyof ActionParameters]: {\n",
        "      params: ActionParameters[K];\n",
        "      result: ActionResults[K];\n",
        "      kind: ActionKinds[K];\n",
        "    };\n",
        "  };\n",
        "  clientActions: {\n",
        "    [K in keyof ClientActionParameters]: {\n",
        "      params: ClientActionParameters[K];\n",
        "      return: ClientActionResults[K];\n",
        "    };\n",
        "  };\n",
        "}\n",
    ));
}
